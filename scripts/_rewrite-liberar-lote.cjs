const fs = require('fs');
const path = require('path');

const target = path.join(
  __dirname,
  '..',
  'components',
  'modals',
  'LiberarLoteModal.tsx'
);
const old = fs.readFileSync(target, 'utf8');
const oldLines = old.split('\n').length;

const lines = [
  "'use client';",
  '',
  "import React, { useState, useEffect, useCallback } from 'react';",
  "import { X, Rocket } from 'lucide-react';",
  'import {',
  '  useLiberarLote,',
  '  type LiberarLoteParams,',
  "} from '@/lib/hooks/useLiberarLote';",
  'import {',
  '  useLiberarLoteEntidade,',
  '  type LiberarLoteEntidadeParams,',
  "} from '@/lib/hooks/useLiberarLoteEntidade';",
  '',
  "import type { EmpresaShort, EntidadeLiberarResponse, LiberarLoteModalProps } from './liberar-lote/types';",
  "import { RhSuccessMessage, EntidadeSuccessMessage } from './liberar-lote/SuccessMessages';",
  "import { LoteErrorMessage } from './liberar-lote/LoteErrorMessage';",
  "import { LoteForm } from './liberar-lote/LoteForm';",
  '',
  'export function LiberarLoteModal({',
  '  isOpen,',
  '  onClose,',
  '  empresaId,',
  '  empresaNome,',
  '  onSuccess,',
  "  mode = 'rh',",
  '}: LiberarLoteModalProps) {',
  "  const isEntidade = mode === 'entidade';",
  '',
  '  const rhHook = useLiberarLote();',
  '  const entidadeHook = useLiberarLoteEntidade();',
  '',
  '  const loadingState = isEntidade ? entidadeHook.loading : rhHook.loading;',
  '  const errorState = isEntidade ? entidadeHook.error : rhHook.error;',
  '  const resultState = isEntidade ? entidadeHook.result : rhHook.result;',
  '  const resetState = isEntidade ? entidadeHook.reset : rhHook.reset;',
  '',
  "  const [descricao, setDescricao] = useState('');",
  "  const [tipo, setTipo] = useState<'completo' | 'operacional' | 'gestao'>('completo');",
  "  const [dataFiltro, setDataFiltro] = useState('');",
  '  const [showSuccess, setShowSuccess] = useState(false);',
  '  const [entidadeResponse, setEntidadeResponse] = useState<EntidadeLiberarResponse | null>(null);',
  '',
  '  const [empresas, setEmpresas] = useState<EmpresaShort[]>([]);',
  '  const [empresaIdLocal, setEmpresaIdLocal] = useState<number | null>(empresaId || null);',
  "  const [empresaNomeLocal, setEmpresaNomeLocal] = useState<string>(empresaNome || '');",
  '  const [companyError, setCompanyError] = useState<string | null>(null);',
  '',
  '  useEffect(() => {',
  '    if (isOpen) {',
  "      setDescricao('');",
  "      setTipo('completo');",
  "      setDataFiltro('');",
  '      setShowSuccess(false);',
  '      setCompanyError(null);',
  '      if (!empresaId) {',
  '        setEmpresaIdLocal(null);',
  "        setEmpresaNomeLocal('');",
  '      }',
  '      resetState();',
  '      setEntidadeResponse(null);',
  '    }',
  '  }, [isOpen, resetState, empresaId]);',
  '',
  '  useEffect(() => {',
  '    if (!empresaId && !isEntidade) {',
  "      fetch('/api/rh/empresas')",
  '        .then((res) => {',
  "          if (!res.ok) throw new Error('Falha ao buscar empresas');",
  '          return res.json();',
  '        })',
  '        .then((data) => setEmpresas(data || []))',
  "        .catch((err) => console.error('Erro ao carregar empresas:', err));",
  '    }',
  '  }, [empresaId, isEntidade]);',
  '',
  '  const handleClose = useCallback(() => {',
  '    if (!loadingState) onClose();',
  '  }, [loadingState, onClose]);',
  '',
  '  const handleSubmit = async (e: React.FormEvent) => {',
  '    e.preventDefault();',
  '    if (loadingState) return;',
  '',
  '    if (isEntidade) {',
  '      const params: LiberarLoteEntidadeParams = { tipo };',
  '      if (descricao.trim()) params.descricao = descricao.trim();',
  '      if (dataFiltro) params.dataFiltro = dataFiltro;',
  '      const response = await entidadeHook.liberarLote(params);',
  '      if (response && response.success) {',
  '        setShowSuccess(true);',
  '        setEntidadeResponse(response);',
  '        onSuccess?.(-1);',
  '      }',
  '      return;',
  '    }',
  '',
  '    const finalEmpresaId = empresaId ?? empresaIdLocal;',
  '    if (!finalEmpresaId) {',
  "      setCompanyError('Selecione uma empresa para continuar');",
  '      return;',
  '    }',
  '',
  '    const params: LiberarLoteParams = { empresaId: finalEmpresaId, tipo };',
  '    if (descricao.trim()) params.descricao = descricao.trim();',
  '    if (dataFiltro) params.dataFiltro = dataFiltro;',
  '',
  '    const response = await rhHook.liberarLote(params);',
  '    if (response && response.success) {',
  '      setShowSuccess(true);',
  '      onSuccess?.((response as any).lote?.id ?? -1);',
  '    }',
  '  };',
  '',
  '  if (!isOpen) return null;',
  '',
  '  return (',
  '    <div',
  '      className="fixed inset-0 z-[1001] flex items-center justify-center bg-black bg-opacity-50 p-4"',
  '      onClick={(e) => { if (e.target === e.currentTarget && !loadingState) handleClose(); }}',
  '      role="dialog"',
  '      aria-modal="true"',
  '      aria-labelledby="modal-title"',
  '    >',
  '      <div',
  '        className="bg-white rounded-lg shadow-xl max-w-2xl w-full max-h-[90vh] overflow-y-auto"',
  '        onClick={(e) => e.stopPropagation()}',
  '      >',
  '        {/* Header */}',
  '        <div className="sticky top-0 bg-white border-b border-gray-200 px-6 py-4 flex items-center justify-between">',
  '          <div className="flex items-center gap-3">',
  '            <Rocket className="text-primary" size={24} />',
  '            <div>',
  '              <h2 id="modal-title" className="text-xl font-bold text-gray-900">',
  '                Iniciar Ciclo de Coletas Avaliativas',
  '              </h2>',
  '              <p className="text-sm text-gray-600">',
  '                {isEntidade',
  "                  ? empresaNome || empresaNomeLocal || 'Entidade \\u2014 apenas funcion\\u00E1rios da minha institui\\u00E7\\u00E3o'",
  "                  : empresaNome || empresaNomeLocal || 'Selecione a empresa'}",
  '              </p>',
  '            </div>',
  '          </div>',
  '          <button',
  '            onClick={handleClose}',
  '            disabled={loadingState}',
  '            className="text-gray-400 hover:text-gray-600 disabled:opacity-50 disabled:cursor-not-allowed"',
  '            aria-label="Fechar modal"',
  '          >',
  '            <X size={24} />',
  '          </button>',
  '        </div>',
  '',
  '        {/* Success Messages */}',
  '        {showSuccess && !isEntidade && <RhSuccessMessage result={resultState} />}',
  '        {showSuccess && isEntidade && (',
  '          <EntidadeSuccessMessage',
  '            entidadeResponse={entidadeResponse}',
  '            result={resultState as EntidadeLiberarResponse | null}',
  '          />',
  '        )}',
  '',
  '        {/* Error Message */}',
  '        {errorState && !showSuccess && (',
  '          <LoteErrorMessage',
  '            error={errorState}',
  '            isEntidade={isEntidade}',
  '            errorHint={!isEntidade ? rhHook.errorHint : undefined}',
  '            entidadeResultDetalhes={',
  '              isEntidade && entidadeHook.result ? (entidadeHook.result as any).detalhes : undefined',
  '            }',
  '          />',
  '        )}',
  '',
  '        {/* Form */}',
  '        {!showSuccess && (',
  '          <LoteForm',
  '            isEntidade={isEntidade}',
  '            loading={loadingState}',
  '            showEmpresaSelector={!empresaId && !isEntidade}',
  '            empresas={empresas}',
  '            empresaIdLocal={empresaIdLocal}',
  '            onEmpresaChange={(id, nome) => {',
  '              setEmpresaIdLocal(id);',
  '              setEmpresaNomeLocal(nome);',
  '              setCompanyError(null);',
  '            }}',
  '            companyError={companyError}',
  '            tipo={tipo}',
  '            setTipo={setTipo}',
  '            descricao={descricao}',
  '            setDescricao={setDescricao}',
  '            dataFiltro={dataFiltro}',
  '            setDataFiltro={setDataFiltro}',
  '            resultState={resultState}',
  '            onSubmit={handleSubmit}',
  '            onClose={handleClose}',
  '          />',
  '        )}',
  '      </div>',
  '    </div>',
  '  );',
  '}',
  '',
];

fs.writeFileSync(target, lines.join('\n'), 'utf8');
const newLines = lines.length;
console.log(
  `LiberarLoteModal.tsx rewritten: ${newLines} lines (was ${oldLines})`
);
