  '',
  '  if (!isOpen) return null;',
  '',
  "  const responsavelLabel = tipo === 'clinica' ? 'Gestor' : 'Respons\\u00E1vel';",
  '',
  '  const handleClose = () => {',
  '    resetarFormulario();',
  '    onClose();',
  '  };',
