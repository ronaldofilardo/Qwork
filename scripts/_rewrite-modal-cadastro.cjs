  '                    </div>',
  '                  )}',
  '',
  "                  {etapaAtual !== 'confirmacao' ? (",
  '                    <button',
  '                      type="button"',
  '                      onClick={avancarEtapa}',
  '                      className="px-4 py-2 border border-gray-300 rounded-md text-gray-700 hover:bg-gray-50 disabled:opacity-50 disabled:cursor-not-allowed transition-colors"',
  "                      disabled={enviando || (etapaAtual === 'contrato' && !contratoAceito) || (etapaAtual === 'dados' && !!cnpjError)}",
  "                      title={etapaAtual === 'contrato' && !contratoAceito ? 'Voc\\u00EA precisa aceitar o contrato para continuar' : undefined}",
