  '              onClose={handleClose}',
  '            />',
  '          ) : (',
  '            <>',
  "              {etapaAtual === 'tipo' && <TipoStep tipo={tipo} setTipo={setTipo} />}",
  '',
  "              {etapaAtual === 'plano' && (",
  '                <PlanoStep',
  '                  planos={planos}',
  '                  planoSelecionado={planoSelecionado}',
  '                  setPlanoSelecionado={setPlanoSelecionado}',
  '                  numeroFuncionarios={numeroFuncionarios}',
  '                  setNumeroFuncionarios={setNumeroFuncionarios}',
  '                />',
  '              )}',
  '',
  "              {etapaAtual === 'dados' && (",
  '                <DadosStep',
  '                  dadostomador={dadosContratante}',
  '                  arquivos={arquivos}',
  '                  cnpjError={cnpjError}',
  '                  onChange={handleDadosChange}',
  '                  onFileChange={handleFileChange}',
  '                />',
  '              )}',
  '',
  "              {etapaAtual === 'contrato' && (",
  '                <ContratoStep',
  '                  contratoGerado={contratoGerado}',
  '                  contratoAceito={contratoAceito}',
  '                  setContratoAceito={setContratoAceito}',
  '                />',
  '              )}',
  '',
  "              {etapaAtual === 'responsavel' && (",
  '                <ResponsavelStep',
  '                  dadosResponsavel={dadosResponsavel}',
  '                  arquivos={arquivos}',
  '                  onChange={handleResponsavelChange}',
  '                  onFileChange={handleFileChange}',
  '                />',
