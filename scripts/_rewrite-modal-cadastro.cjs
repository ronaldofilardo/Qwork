  '    try {',
  '      const res = await submit();',
  '      if (res && res.redirect) {',
  '        setTimeout(() => { window.location.href = res.redirect as string; }, 100);',
  '      }',
  '    } catch (err) {',
  "      console.error('Erro ao submeter formul\\u00E1rio:', err);",
  '    }',
  '  };',
  '',
  '  return (',
  '    <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50 p-4">',
